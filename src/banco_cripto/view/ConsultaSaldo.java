    private void ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarActionPerformed
        String senhaDigitada = txtSenha.getText().trim();

        if (senhaDigitada.equals(investidorLogado.getSenha())) {
            // Exibe o saldo em reais
            CampoSaldo.setText(String.format("R$ %.2f", investidorLogado.getCarteira().getSaldoReais()));

            // Exibe a quantidade de cada criptomoeda
            double saldoBitcoin = investidorLogado.getCarteira().getSaldoBitcoin();
            double saldoEthereum = investidorLogado.getCarteira().getSaldoEthereum();
            double saldoRipple = investidorLogado.getCarteira().getSaldoRipple();

            // Exibe as quantidades de criptomoedas na Ã¡rea de texto
            CampoSaldo.append("\nCriptomoedas em carteira:");
            CampoSaldo.append(String.format("\nBitcoin: %.4f BTC", saldoBitcoin));
            CampoSaldo.append(String.format("\nEthereum: %.4f ETH", saldoEthereum));
            CampoSaldo.append(String.format("\nRipple: %.4f XRP", saldoRipple));

        } else {
            JOptionPane.showMessageDialog(this, "Senha incorreta! Tente novamente.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ConsultarActionPerformed

